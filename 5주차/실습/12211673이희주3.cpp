//cmath function

#include <iostream>
#include <cmath>
using namespace std;

int main() {
	double x;

	cout << "input real number 'x':";
	cin >> x;

	cout << "ceil(x)=" << ceil(x)<<endl;
	cout << "exp(x)=" << exp(x)<<endl;
	cout << "log(x)=" << log(x)<<endl;
	cout << "sqrt(x)=" << sqrt(x) << endl;
	cout << "fabs(x)=" << fabs(x)<<endl;
	cout << "pow(x,2)=" << pow(x, 2);

}
